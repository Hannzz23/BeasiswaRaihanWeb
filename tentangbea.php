        Beasiswa adalah salah satu bentuk bantuan berupa uang baik dari pemerintah maupun instansi swasta yang digunakan
        untuk membantu serta menunjang pendidikan siswa / mahasiswa di Indonesia. Salah satu yang terkenal adalah Kampus Merdeka.
