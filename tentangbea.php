        sejak siswa / mahasiswa tersebut terdaftar hingga akhir pendidikan mereka di jenjang yang sedang dijalaninya.
        Tentunya tidak semua pelajar dapat menerima beasiswa, hanya beberapa siswa yang lolos dalam syarat yang telah ditentukan yang bisa mendapat beasiswa.
        Ada juga yang beberapa instansi atau pemerintah yang mewajibkan pendaftar beasiswanya untuk melakukan ujian terlebih dahulu lalu diseleksi
        siapa saja yang layak mendapat besiswa.
